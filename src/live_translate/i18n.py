FAILURE_TOKENS = ("실패", "失敗", "Failed", "failed")


class Translator:
    def __init__(self, language: str = "ko") -> None:
        self.language = language if language in SUPPORTED_LANGUAGES else "ko"

    def t(self, key: str, **kwargs: object) -> str:
        table = TRANSLATIONS.get(self.language, TRANSLATIONS["ko"])
        text = table.get(key, TRANSLATIONS["ko"].get(key, key))
        if kwargs:
            return text.format(**kwargs)
        return text

    def runtime_description(self, dependency_name: str, fallback: str) -> str:
        key = RUNTIME_DESC_KEYS.get(dependency_name)
        text = self.t(key) if key else fallback
        if dependency_name in SLOW_RUNTIME_DEPENDENCIES:
            text = f"{text} {self.t('runtime.desc.slow_install')}"
        return text

    def is_error_message(self, message: str) -> bool:
        return any(message.startswith(prefix) for prefix in ERROR_PREFIXES)

    def is_busy_message(self, message: str) -> bool:
        if self.is_error_message(message):
            return False
        if any(token in message for token in ("완료", "完了", "complete", "Complete")):
            return False
        if any(token in message for token in FAILURE_TOKENS):
            return False
        return any(token in message for token in BUSY_STATUS_TOKENS)


