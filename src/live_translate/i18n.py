def translate_driver_message(translator: Translator, message: str) -> str:
    if not message:
        return message
    try:
        from .gpu_driver import query_nvidia_driver, RUNTIME_CUDA_REQUIREMENT, minimum_driver_for_cuda, format_version
    except ImportError:
        return message

    driver = query_nvidia_driver()
    if "NVIDIA GPU가 없거나" in message or "NVIDIA GPUがないか" in message or "No NVIDIA GPU" in message:
        return translator.t("gpu.no_nvidia")
    if "GPU 호환 확인됨" in message or "GPU互換確認済み" in message or message.startswith("GPU compatible"):
        if driver:
            gpus = ", ".join(driver.gpu_names) if driver.gpu_names else "NVIDIA GPU"
            return translator.t(
                "gpu.compatible",
                gpus=gpus,
                driver=driver.driver_version_raw,
                cuda=driver.cuda_version_raw or RUNTIME_CUDA_REQUIREMENT,
            )
        return message
    if "NVIDIA 드라이버 버전이 낮아" in message or "NVIDIAドライバーのバージョンが低く" in message or "driver version is too low" in message:
        return _format_driver_upgrade(translator, driver)
    if "NVIDIA GPU 드라이버를 확인할 수 없습니다" in message or "NVIDIA GPUドライバーを確認できません" in message or "Unable to verify NVIDIA GPU driver" in message:
        required = format_version(minimum_driver_for_cuda())
        return translator.t("gpu.not_found", cuda=RUNTIME_CUDA_REQUIREMENT, driver=required)
    return message


def _format_driver_upgrade(translator: Translator, info) -> str:
    from .gpu_driver import RUNTIME_CUDA_REQUIREMENT, minimum_driver_for_cuda, format_version

    required = format_version(minimum_driver_for_cuda())
    if info is None:
        return translator.t("gpu.not_found", cuda=RUNTIME_CUDA_REQUIREMENT, driver=required)
    lines = [
        translator.t("gpu.driver_low_title"),
        translator.t("gpu.driver_current", version=info.driver_version_raw or format_version(info.driver_version)),
        translator.t("gpu.driver_required", version=required),
        translator.t("gpu.cuda_required", version=RUNTIME_CUDA_REQUIREMENT),
    ]
    if info.cuda_version_raw:
        lines.append(translator.t("gpu.cuda_supported", version=info.cuda_version_raw))
    if info.gpu_names:
        lines.append(translator.t("gpu.detected", gpus=", ".join(info.gpu_names)))
    lines.append(translator.t("gpu.update_hint"))
    return "\n".join(lines)
